    private void btn_updateScanStatus_cancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateScanStatus_cancelActionPerformed
       
        //if scan is finished
        if(scanFinished){
            
            MainMenu m1 = new MainMenu();
            m1.setVisible(true);
            this.dispose();
            
        }else{
            int userReply;

            userReply = JOptionPane.showConfirmDialog(null,"Are you sure you want cancel the virus database update?", "Cancel Database Update", JOptionPane.YES_NO_OPTION);

            if(userReply == JOptionPane.YES_OPTION){

                cancelStatus = 0;

                JOptionPane updateCancelOptionPane = new JOptionPane("Database Update Cancelled!", JOptionPane.INFORMATION_MESSAGE);

                JDialog updateCancelDialog = updateCancelOptionPane.createDialog(null, "ClamWin Free Antivirus");
                updateCancelDialog.setModal(true);
                updateCancelDialog.setVisible(true);

                MainMenu m1 = new MainMenu();
                m1.setVisible(true);
                this.dispose();

            }else{

            }
        }
        

    }//GEN-LAST:event_btn_updateScanStatus_cancelActionPerformed

