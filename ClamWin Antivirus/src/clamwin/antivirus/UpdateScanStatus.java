    //progress bar function
    public void progressBar(){

        //lambda thread creation
        Thread t1 = new Thread(() -> {
            for(int i = 0; i <= 100; i++) {
                try {
                    Thread.sleep(50);
                    lbl_updateScanStatus_inside_complete.setText(Integer.toString(i)+"% complete");
                    progressBar_updateScanStatus_inside.setValue(i);
                    
                    //if cancelled stop the scanning
                    if(cancelStatus == 0){
                        break;
                    }
                    
                } catch (Exception e) {
                    e.printStackTrace();
                }
        }
           
        //only display message if not cancelled by the user    
        if(cancelStatus != 0){
            
            scanFinished = true;
            
            //success message
            JOptionPane scanSuccessOptionPane = new JOptionPane("The virus database has been updated successfully !", JOptionPane.INFORMATION_MESSAGE);

            JDialog scanSuccessDialog = scanSuccessOptionPane.createDialog(null, "ClamWin Free Antivirus");
            scanSuccessDialog.setModal(true);
            scanSuccessDialog.setVisible(true);
            
        }
                
        });

        t1.start();
                        
    }
