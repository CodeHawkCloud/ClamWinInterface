    //progress bar function
    public void progressBar(){

        //lambda thread creation
        Thread t1 = new Thread(() -> {
            for(int i = 0; i <= 100; i++) {
                try {
                    Thread.sleep(50);
                    lbl_selectedScanStatus_inside_complete.setText(Integer.toString(i)+"% complete");
                    progressBar_selectedScanStatus_inside.setValue(i);
                    
                    //if cancelled stop the scanning
                    if(cancelStatus == 0){
                        break;
                    }
            
                } catch (Exception e) {
                    e.printStackTrace();
                }
        }
               
            //only display message if not cancelled by the user    
            if(cancelStatus != 0){    

                scanFinished = true;
                
                JOptionPane scanSuccessOptionPane = new JOptionPane("The selected files have been scanned successfully !", JOptionPane.INFORMATION_MESSAGE);

                JDialog scanSuccessDialog = scanSuccessOptionPane.createDialog(null, "ClamWin Free Antivirus");
                scanSuccessDialog.setModal(true);
                scanSuccessDialog.setVisible(true);
            }        
        });

        t1.start();
                        
    }
    


