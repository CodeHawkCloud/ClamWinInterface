    //cancel button mouse click action
    private void btn_selectedScanStatus_cancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_selectedScanStatus_cancelActionPerformed
        
        //if scan is finished
        if(scanFinished){
            
                SelectedFiles sf1 = new SelectedFiles();
                sf1.setVisible(true);
                this.dispose();
            
        }else{
            
            int userReply;

            userReply = JOptionPane.showConfirmDialog(null,"Are you sure you want to cancel the file scanning?", "Cancel File Scanning", JOptionPane.YES_NO_OPTION);

            if(userReply == JOptionPane.YES_OPTION){

                cancelStatus = 0;

                JOptionPane savingSuccessOptionPane = new JOptionPane("File scanning cancelled !", JOptionPane.INFORMATION_MESSAGE);

                JDialog savingSuccessDialog = savingSuccessOptionPane.createDialog(null, "ClamWin Free Antivirus");
                savingSuccessDialog.setModal(true);
                savingSuccessDialog.setVisible(true);

                SelectedFiles sf1 = new SelectedFiles();
                sf1.setVisible(true);
                this.dispose();

            }else{


            }
        }
        

    }//GEN-LAST:event_btn_selectedScanStatus_cancelActionPerformed

